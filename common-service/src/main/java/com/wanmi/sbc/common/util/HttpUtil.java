    /**
     * @param host
     * @param path
     * @param headers
     * @param querys
     * @param body
     * @return
     * @Description:使用JSON请求 并设置超时时间
     * @author qinrui.zhu
     * @version 1.0
     * @date 2019/1/14 12:30
     */
    public static HttpResponse doPost(String host, String path, Map<String, String> headers,
                                      Map<String, String> querys, String body) throws Exception {
        HttpClient httpClient = wrapClient(host);
        // 设置超时时间
        httpClient.getParams().setParameter(CoreConnectionPNames.CONNECTION_TIMEOUT, 3000);
        httpClient.getParams().setParameter(CoreConnectionPNames.SO_TIMEOUT, 3000);
        //在提交请求之前 测试连接是否可用
        httpClient.getParams().setBooleanParameter(CoreConnectionPNames.STALE_CONNECTION_CHECK, true);

        HttpPost request = new HttpPost(buildUrl(host, path, querys));
        for (Map.Entry<String, String> e : headers.entrySet()) {
            request.addHeader(e.getKey(), e.getValue());
        }

        if (StringUtils.isNotBlank(body)) {
            request.setEntity(new StringEntity(body, "utf-8"));
        }

        return httpClient.execute(request);
    }

